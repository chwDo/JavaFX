package Model;

/**
 *
 * @author Administrator
 */
public class Timu {
        private String Title_Types;
        private String Title_Contents;
        private int idex;
        private int Scores;
        private String answer;
        private String type;
        
    /**
     * @return the Title_Types
     */
    public String getTitle_Types() {
        return Title_Types;
    }

    /**
     * @param Title_Types the Title_Types to set
     */
    public void setTitle_Types(String Title_Types) {
        this.Title_Types = Title_Types;
    }

    /**
     * @return the Title_Contents
     */
    public String getTitle_Contents() {
        return Title_Contents;
    }

    /**
     * @param Title_Contents the Title_Contents to set
     */
    public void setTitle_Contents(String Title_Contents) {
        this.Title_Contents = Title_Contents;
    }

    /**
     * @return the idex
     */
    public int getIdex() {
        return idex;
    }

    /**
     * @param idex the idex to set
     */
    public void setIdex(int idex) {
        this.idex = idex;
    }

    /**
     * @return the Scores
     */
    public int getScores() {
        return Scores;
    }

    /**
     * @param Scores the Scores to set
     */
    public void setScores(int Scores) {
        this.Scores = Scores;
    }

    /**
     * @return the answer
     */
    public String getAnswer() {
        return answer;
    }

    /**
     * @param answer the answer to set
     */
    public void setAnswer(String answer) {
        this.answer = answer;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

}
